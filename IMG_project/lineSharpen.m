%% ================= Line Sharpening Filters =================

% ---------------- Horizontal Line Sharpen -----------------
function out = lineSharpenH(img)

        if size(img,3)==3
        img = rgb2gray(img);
    end

    % ????? Kernel ?????? ???????
    H = [-1 -1 -1; 2 2 2; -1 -1 -1];

    out = imfilter(double(img), H, 'replicate');
end

